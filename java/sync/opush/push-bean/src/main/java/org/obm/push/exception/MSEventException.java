package org.obm.push.exception;

public class MSEventException extends MSObjectException {

	public MSEventException() {
		super();
	}

	public MSEventException(String message, Throwable cause) {
		super(message, cause);
	}

	public MSEventException(String message) {
		super(message);
	}

	public MSEventException(Throwable cause) {
		super(cause);
	}
}
